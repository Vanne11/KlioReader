/** Recursively widen literal strings to `string` and readonly tuples to `string[]` */
type Widen<T> = T extends readonly string[]
  ? readonly string[]
  : T extends string
    ? string
    : T extends object
      ? { [K in keyof T]: Widen<T[K]> }
      : T;

